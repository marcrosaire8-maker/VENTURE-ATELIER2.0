-- =========================================================================
-- SECURE LOGIN RPC FUNCTION (EXPLICIT PASSWORD ENCRYPTION VALIDATION ON SERVER)
-- =========================================================================

create or replace function check_admin_login(p_email text, p_password text)
returns boolean as $$
declare
    v_match boolean;
begin
    select (password_hash = crypt(p_password, password_hash)) into v_match
    from va_users
    where email = p_email;
    return coalesce(v_match, false);
end;
$$ language plpgsql security definer;

-- Grant execution to public so anon user can call it
grant execute on function check_admin_login(text, text) to anon;
grant execute on function check_admin_login(text, text) to authenticated;


-- =========================================================================
-- INITIALISATION DE SUPABASE STORAGE ET POLITIQUES D'ACCÈS
-- =========================================================================

-- 1. Configuration du bucket de stockage d'actifs "venture_atelier"
insert into storage.buckets (id, name, public)
values ('venture_atelier', 'venture_atelier', true)
on conflict (id) do nothing;

-- Nettoyage des politiques existantes pour éviter des erreurs d'initialisation répétée
drop policy if exists "Médiathèque - Lecture publique" on storage.objects;
drop policy if exists "Médiathèque - Téléversement Admin" on storage.objects;
drop policy if exists "Médiathèque - Modification Admin" on storage.objects;
drop policy if exists "Médiathèque - Suppression Admin" on storage.objects;

-- 2. Sécurisation : Politique pour autoriser l'accès en lecture à tout le monde
create policy "Médiathèque - Lecture publique" on storage.objects
    for select using (bucket_id = 'venture_atelier');

-- 3. Sécurisation : Politique pour autoriser aux administrateurs connectés les téléversements
create policy "Médiathèque - Téléversement Admin" on storage.objects
    for insert with check (
        bucket_id = 'venture_atelier' 
        and auth.role() = 'authenticated'
    );

-- 4. Sécurisation : Politique pour autoriser aux administrateurs connectés la modification
create policy "Médiathèque - Modification Admin" on storage.objects
    for update using (
        bucket_id = 'venture_atelier' 
        and auth.role() = 'authenticated'
    );

-- 5. Sécurisation : Politique pour autoriser aux administrateurs connectés la suppression
create policy "Médiathèque - Suppression Admin" on storage.objects
    for delete using (
        bucket_id = 'venture_atelier' 
        and auth.role() = 'authenticated'
    );

