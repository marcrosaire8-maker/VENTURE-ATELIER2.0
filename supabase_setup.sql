-- 6. TABLE DE TRAÇABILITÉ COMPLÈTE ET SÉCURISÉE (JOURNAUX D'AUDIT SÉCURITÉ)
-- =========================================================================

create table if not exists va_audit_logs (
    id bigint generated by default as identity primary key,
    timestamp timestamp with time zone default timezone('utc'::text, now()),
    event_type text not null,          -- AUTH_SUCCESS, AUTH_FAILURE, CONFIG_UPDATE, PROJECT_CREATE, etc.
    actor text not null,               -- 'ventureatelier@gmail.com', 'visiteur', ou 'système'
    description text not null,          -- Détails de l'action effectuée
    ip_address text,                   -- IP de l'utilisateur ou 'Inconnue'
    user_agent text,                   -- User-Agent du navigateur
    status text not null,              -- 'SUCCESS', 'FAILURE', 'WARNING'
    metadata jsonb default '{}'::jsonb  -- Données complémentaires cryptographiques ou payloads de traçabilité
);

-- Activation sécurité au niveau ligne (RLS)
alter table va_audit_logs enable row level security;

-- Nettoyage des politiques existantes pour éviter des erreurs d'initialisation répétée
drop policy if exists "va_audit_logs_insert_policy" on va_audit_logs;
drop policy if exists "va_audit_logs_select_policy" on va_audit_logs;

-- Insertion autorisée sans restrictions pour enregistrer toutes les actions (formulaires, échecs d'auth, etc)
create policy "va_audit_logs_insert_policy" on va_audit_logs 
    for insert with check (true);

-- Lecture réservée exclusivement aux administrateurs d'Atelier connectés
create policy "va_audit_logs_select_policy" on va_audit_logs 
    for select using (auth.role() = 'authenticated');



